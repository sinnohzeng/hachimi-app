/// AppIconSize — Material Design 3 icon size tokens.
///
/// M3 Icon Scale:
///   micro (16dp) — 紧凑指示器、Badge 内图标
///   small (20dp) — 列表辅助图标、Chip 内图标
///   standard (24dp) — 默认图标尺寸（IconButton、AppBar）
///   large (40dp) — 强调图标、空状态辅助
///   extraLarge (56dp) — 精选卡片、头像区域
///   hero (64dp) — 空状态主图标、引导页
class AppIconSize {
  AppIconSize._();

  static const double micro = 16;
  static const double small = 20;
  static const double standard = 24;
  static const double large = 40;
  static const double extraLarge = 56;
  static const double hero = 64;
}
